  static final Set<String> _allLocalizationKeys = {
    for (final localeMap in _localizedValues.values) ...localeMap.keys,
  };

  static final Map<String, bool> _translationCompletenessCache =
      _buildTranslationCompletenessCache();

  static Map<String, bool> _buildTranslationCompletenessCache() {
    final result = <String, bool>{};

    for (final entry in _localizedValues.entries) {
      final code = entry.key;
      final localeMap = entry.value;

      if (code == 'en') {
        result[code] = true;
        continue;
      }

      var isComplete = true;
      for (final key in _allLocalizationKeys) {
        final value = localeMap[key];
        if (value == null || value.trim().isEmpty || value == key) {
          isComplete = false;
          break;
        }
      }
      result[code] = isComplete;
    }

    return result;
  }

  static bool isLanguageFullyTranslated(String languageCode) {
    return _translationCompletenessCache[languageCode] ?? false;
  }

  String tr(String key, {Map<String, String>? params}) {
    final lang = _localizedValues.containsKey(locale.languageCode)
        ? locale.languageCode
        : 'en';
    var value = _localizedValues[lang]?[key] ?? key;

    if (params != null) {
      for (final entry in params.entries) {
        value = value.replaceAll('{${entry.key}}', entry.value);
      }
    }

    return value;
  }
